func resolveSyntaxColors(entries chroma.StyleEntries) map[chroma.TokenType]string {
	return flattenTokenColors(chroma.MustNewStyle("local", entries))
}

func flattenTokenColors(s *chroma.Style) map[chroma.TokenType]string {
	out := make(map[chroma.TokenType]string, len(chroma.StandardTypes))
	for tt := range chroma.StandardTypes {
		if c := s.Get(tt).Colour; c.IsSet() {
			out[tt] = c.String()
		}
	}
	return out
}
